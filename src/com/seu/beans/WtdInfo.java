package com.seu.beans;

public class WtdInfo {
	private int id;
	private String wtdnum;
	private String sjljh;
	private String version;
	private int state;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getWtdnum() {
		return wtdnum;
	}
	public void setWtdnum(String wtdnum) {
		this.wtdnum = wtdnum;
	}
	public String getSjljh() {
		return sjljh;
	}
	public void setSjljh(String sjljh) {
		this.sjljh = sjljh;
	}
	public String getVersion() {
		return version;
	}
	public void setVersion(String version) {
		this.version = version;
	}
	public int getState() {
		return state;
	}
	public void setState(int state) {
		this.state = state;
	}

	
	
}
